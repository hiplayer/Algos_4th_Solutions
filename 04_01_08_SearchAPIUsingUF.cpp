class Graph {
private:

    vector< forward_list< int > > adj;
    int V = 0;
    int E = 0;

public:

    Graph( int V )
            : V( V )
    {
        adj.resize( V );
    }

    virtual ~Graph() {
        // not implemented
    }

    int getV() const {
        return V;
    }

    int getE() const {
        return E;
    }

    forward_list< int > getAdj( int v ) const {
        return adj[ v ];
    }

    void addEdge( int v, int w ) {
        adj[ v ].push_front( w );
        adj[ w ].push_front( v );
        ++E;
    }

    string toString() const {
        string rv = "\nV : " + to_string( V ) + " ; E : " + to_string( E ) + " ; adj : \n";
        for ( int v = 0; v < V; ++v ) {
            rv += "\n| v : " + to_string( v ) + " : ";
            for ( int w : getAdj( v ) ) {
                rv += to_string( w ) + ", ";
            }
            rv += " | ";
        }
        return rv;
    }

    static int degree( const Graph& G, int v ) {
        int deg = 0;
        for ( int w : G.getAdj( v ) ) {
            ++deg;
        }
        return deg;
    }

    static int maxDegree( const Graph& G ) {
        int maxDeg = 0;
        for ( int v = 0; v < G.getV(); ++v ) {
            int deg = degree( G, v );
            if ( maxDeg < deg ) {
                maxDeg = deg;
            }
        }
        return maxDeg;
    }

    static int avgDegree( const Graph& G ) {
        return 2 * G.getE() / G.getV();
    }

    static int numSelfLoops( const Graph& G ) {
        int count = 0;
        for ( int v = 0; v < G.getV(); ++v ) {
            for ( int w : G.getAdj( v ) ) {
                if ( v == w ) {
                    ++count;
                }
            }
        }
        return count / 2;
    }

};

class SearchUF {
private:

    vector< int > id;
    vector< int > sz;
    int count = 0;
    int s = 0;

public:

    SearchUF( const Graph& G, int s )
        : s( s ),
          count( G.getV() )
    {
        id.resize( G.getV() );
        sz.resize( G.getV() );
        for ( int i = 0; i < id.size(); ++i ) {
            id[ i ] = i;
            sz[ i ] = 1;
        }

        for ( int v = 0; v < G.getV(); ++v ) {
            for ( int w : G.getAdj( v ) ) {
                doUnion( v, w );
            }
        }
    }

    virtual ~SearchUF() {
        // not implemented
    }

    int find( int p ) {
        while ( p != id[ p ] ) p = id[ p ];
        return p;
    }

    void doUnion( int p, int q ) {
        int i = find( p );
        int j = find( q );

        if ( i == j ) return;

        if ( sz[ i ] < sz[ j ] ) {
            id[ i ] = j;
            sz[ j ] += sz[ i ];
        } else {
            id[ j ] = i;
            sz[ i ] += sz[ j ];
        }
        --count;
    }

    int connected( int p, int q ) {
        return find( p ) == find( q );
    }

    bool marked( int v ) {
        return connected( v, s );
    }

    int getCount() {
        return sz[ s ];
    }

    string toString() const {
        string rv = "\n";
        for ( int i = 0; i < id.size(); ++i ) {
            rv += "| i : " + to_string( i ) + " : " + to_string( id[ i ] ) + " | , ";
        }
        return rv;
    }
};

